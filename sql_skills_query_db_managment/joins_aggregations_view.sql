SELECT CUSTOMER_ID, COUNT(*) AS TotalOrders
FROM ORDER_TYPE
GROUP BY CUSTOMER_ID;

SELECT AUTHOR_ID, AVG(BOOK_PRICE) AS AveragePrice
FROM BOOK
GROUP BY AUTHOR_ID;

SELECT AUTHOR_ID, MAX(BOOK_PRICE) AS MaxPrice
FROM BOOK
GROUP BY AUTHOR_ID;

SELECT MANGA_ID, MANGA_TITLE, MANGA_PRICE, 
       MANGA_PRICE * 0.9 AS PriceAfterDiscount, 
       (MANGA_PRICE * 0.9) * 1.05 AS FinalPrice
FROM MANGA;

SELECT BOOK_ID, BOOK_TITLE, BOOK_PRICE, 
       BOOK_PRICE - (SELECT AVG(BOOK_PRICE) FROM BOOK) AS PriceDifference
FROM BOOK;


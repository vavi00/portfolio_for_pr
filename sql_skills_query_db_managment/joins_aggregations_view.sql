SELECT BOOK_TITLE, AUTHOR_ID, BOOK_PRICE
FROM BOOK
WHERE BOOK_PRICE BETWEEN 11 AND 13;

SELECT MANGA_TITLE, MANGA_PRICE
FROM MANGA
WHERE MANGA_TITLE LIKE '[MNK]%';

SELECT JOURNAL_ID, JOURNAL_TITLE, JOURNAL_PRICE
FROM JOURNAL
ORDER BY JOURNAL_PRICE DESC;

SELECT O.ORDER_ID, C.CUSTOMER_NAME, C.CUSTOMER_SURNAME, B.BOOK_TITLE, O.AMOUNT
FROM ORDER_TYPE O
JOIN CUSTOMER C ON O.CUSTOMER_ID = C.CUSTOMER_ID
LEFT JOIN BOOK B ON O.ORDER_ID = B.ORDER_ID;

SELECT O.CUSTOMER_ID, O.AMOUNT, E.ORDER_EXEC_ID, E.DATE_EXEC
FROM ORDER_TYPE O
INNER JOIN ORDER_EXEC E ON O.ORDER_ID = E.ORDER_ID;

SELECT O.ORDER_ID, C.CUSTOMER_NAME, C.CUSTOMER_SURNAME, B.BOOK_TITLE, O.AMOUNT
FROM ORDER_TYPE O
JOIN CUSTOMER C ON O.CUSTOMER_ID = C.CUSTOMER_ID
RIGHT JOIN BOOK B ON O.ORDER_ID = B.ORDER_ID;

